package com.epicode.esercizio3;

public class Articolo {
	
	public int codiceArticolo;
	public String descrizione;
	public int prezzo;
	public int pezzi;
	
	public Articolo(int codiceArticolo, String descrizione, int prezzo, int pezzi) {
		this.codiceArticolo = codiceArticolo;
		this.descrizione = descrizione;
		this.prezzo = prezzo;
		this.pezzi = pezzi;
	}

}
